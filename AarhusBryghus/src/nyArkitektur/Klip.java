package nyArkitektur;

public class Klip {

	private int nr;
	private boolean brugt;
	
	public Klip(int nr) {
		this.nr = nr;
	}

	public int getNr() {
		return nr;
	}

	public void setNr(int nr) {
		this.nr = nr;
	}

	public boolean isBrugt() {
		return brugt;
	}

	public void setBrugt(boolean brugt) {
		this.brugt = brugt;
	}

	
	
}
